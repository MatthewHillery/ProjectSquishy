class CameraSettings:
    def __init__(self, camera_shake, fov, height, angle, distance, stiffness, swivel_speed, transition_speed, ball_camera, last_updated):
        self.camera_shake = camera_shake
        self.fov = fov
        self.height = height
        self.angle = angle
        self.distance = distance
        self.stiffness = stiffness
        self.swivel_speed = swivel_speed
        self.transition_speed = transition_speed
        self.ball_camera = ball_camera
        self.last_updated = last_updated
